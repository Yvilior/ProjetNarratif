                    {
                        Console.WriteLine("Vous ouvrez la porte a votre droite.");
                        porteD = false;
                        Batterie -= 2;
                    }
                    else
                    {
                        Console.WriteLine("Vous fermez la porte a votre droite.");
                        porteD = true;
                        Batterie -= 2;
                        if (isChica || isFreddy)
                        {
                            Console.WriteLine("Quelqu'un tappe a la porte et disparait dans le noir");
                            isFreddy = false; isChica = false;
                        }
                    }
                    break;
                case "fg":
                    Console.WriteLine("Vous allumez la lumiere a votre gauche.");
                    Batterie -= 2;
                    if (isBonnie)
                    {
                        Console.WriteLine("Vous voyez une silouhette");
                        
                    }
                    else
                    {
                        Console.WriteLine("Vous voyez rien");

                    }
                    break;
                case "fd":
                    Console.WriteLine("Vous allumez la lumiere a votre droite.");
                    Batterie -= 2;
                    if (isChica || isFreddy)
                    {
                        Console.WriteLine("Vous voyez une silouhette");

                    }
                    else
                    {
                        Console.WriteLine("Vous voyez rien");

                    }
                    break;
                case "fin":
                    Batterie = 0;
                    break;
                case "secret foxy":
                   isFoxy = true;
                    break;
                case "secret chica":
                    isChica = true;
                    break;
                case "secret bonnie":
                    isBonnie = true;
                    break;
                case "secret freddy":
                    isFreddy = true;
                    break;
                case "attaque gauche":
                    CouloirGauche.isBonnie = true;
                    break;
                case "attaque droite":
                    CouloirDroite.isChica = true;
                    CouloirDroite.isFreddy = true;
                    break;
                case "info on":
                    isInfo = true;
                    Info();
                    break;
                case "info off":
                    isInfo = false;
                    break;
                case "bonus":
                    Batterie = 100;
                    
                    break;
                case "t":
                    Batterie --;
                    CounterAM ++;
                    MouvMechant();
                    Attacke();
                    break;
                case "6":
                    AM = 6;
                    break;
                case "1":
                    AM = 1;
                    break;
                case "5":
                    AM = 5;
                    break;
                case "2":
                    AM = 2;
                    break;
                case "3":
                   AM = 3;
                    break;
                case "4":
                    AM = 4;
                    break;
                default:
                    Console.WriteLine("Commande invalide.");
                    break;
            }
        }

